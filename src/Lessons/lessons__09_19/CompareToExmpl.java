package Lessons.lessons__09_19;

public class CompareToExmpl {
    public static void main(String[] args) {
        String strSample = "a";
        System.out.println("Compare 'a' To b is " + strSample.compareTo("b"));

        strSample = "b";
        System.out.println("Compare 'a' To b is " + strSample.compareTo("a"));

        strSample = "b";
        System.out.println("Compare 'a' To b is " + strSample.compareTo("b"));
    }
}
