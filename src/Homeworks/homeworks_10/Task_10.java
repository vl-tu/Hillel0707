package Homeworks.homeworks_10;

public class Task_10 {
    public static void main(String[] args) {
        for (int i = 1; i <= 1; i++) {
            for (int j = 0; j <= 5; j++) {
                switch (j) {
                    case 1:
                        System.out.println("    *    ");
                        break;
                    case 2:
                        System.out.println("   * *   ");
                        break;
                    case 3:
                        System.out.println("  * * *  ");
                        break;
                    case 4:
                        System.out.println(" * * * * ");
                        break;
                    case 5:
                        System.out.println("* * * * *");
                        break;
                }
            }
        }
        System.out.println();
    }
}
